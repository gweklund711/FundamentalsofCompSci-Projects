# Add two numbers together with this simple addition calculator
print("Welcome to the simple addition calculator. With this program, you can add two numbers together.")
print("What should the first number be?")
numOne = float(input())
print("What should the second number be?")
numTwo = float(input())
numSum = numOne + numTwo
print(f"If you add {numOne} and {numTwo} together, the sum is {round(numSum, 1)} ")